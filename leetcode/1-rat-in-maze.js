const solveMazeDynamically = (maze, cache = { '0-0': 1 }) => {
  maze.forEach((row, rowIndex) => {
    // console.log('rowIndex', rowIndex);

    row.forEach((col, colIndex) => {
      // console.log('colIndex', colIndex);

      if (!col) {
        return;
      }

      if (!cache[`${rowIndex}-${colIndex}`]) {
        cache[`${rowIndex}-${colIndex}`] = 0;
      }

      const leftCount = cache[`${rowIndex}-${colIndex - 1}`] || 0;
      const upCount = cache[`${rowIndex - 1}-${colIndex}`] || 0;

      console.log(`(${rowIndex}-${colIndex})`, leftCount, upCount);

      cache[`${rowIndex}-${colIndex}`] += leftCount + upCount;
    });

    console.log(cache);
  });

  return cache[`${maze.length - 1}-${maze[0].length - 1}`];
};


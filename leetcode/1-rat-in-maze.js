const solveMazeRecursively = (maze, rowIndex, colIndex, numSolves = 0, numInvocations = 0) => {
  this.numInvocations = numInvocations + 1;
  this.numSolves = numSolves;

  // console.log('invocation', this.numInvocations);

  if (rowIndex === maze.length - 1 && colIndex === maze[0].length - 1) {
    this.numSolves += 1;
  }

  if (maze[rowIndex + 1] && maze[rowIndex + 1][colIndex]) {
    solveMazeRecursively(maze, rowIndex + 1, colIndex, this.numSolves);
  }

  if (maze[rowIndex] && maze[rowIndex][colIndex + 1]) {
    solveMazeRecursively(maze, rowIndex, colIndex + 1, this.numSolves);
  }

  return this.numSolves;
};

